public class switch_case {

    public static void main(String[] args) {
        int num =400;
        switch(num)
        {
            case 100 : System.out.println("case1");
            break;
            case 500 : System.out.println("case2");
            break;
            case 300 : System.out.println("case3");
            break;
            default : System.out.println("default");
        }
    }
}
